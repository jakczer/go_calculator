package calc

func Add(a int64, b int64) int64 {
	return a + b
}

func Div(a int64, b int64) int64 {
	return a / b
}

func Mul(a int64, b int64) int64 {
	return a * b
}

func Sub(a int64, b int64) int64 {
	return a - b
}

func Mod(a int64, b int64) int64 {
	return a % b
}

func Eqaul(a int, b int) bool {
	return a == b
}

func NotEqual(a int, b int) bool {
	return a != b
}

func Lt(a int, b int) bool {
	return a < b
}

func Gt(a int, b int) bool {
	return a > b
}

func Leq(a int, b int) bool {
	return a <= b
}

func Geq(a int, b int) bool {
	return a >= b
}
